/**
  * @brief This function handles UART4 global interrupt.
  * @explanation: Raising IDLE flag mechanism at the end of UART DMA stream may
  * save a lot of frustration and MCU memory. UART RX circuit accepts data with
  * unknown size, unless we made some assumptions and it emerges a need to cope
  * with such transmission. One solution is to declare a buffer that became
  * full with probability less than assumed value. The another solution is to
  * exploit an IDLE mechanism. IDLE interrupt works like this: if UART RX line
  * is in idle state for the time of transmitting 1 character, then IDLE flag
  * is raised.
  */
void UART4_IRQHandler(void)
{
  /* USER CODE BEGIN UART4_IRQn 0 */

  /* USER CODE END UART4_IRQn 0 */
  HAL_UART_IRQHandler(&huart4);
  /* USER CODE BEGIN UART4_IRQn 1 */
  if (__HAL_UART_GET_FLAG(&huart4, UART_FLAG_IDLE)) {
	  __HAL_UART_CLEAR_IDLEFLAG(&huart4);
	  HAL_UART_RxCpltCallback(&huart4);
  }
  /* USER CODE END UART4_IRQn 1 */
}

